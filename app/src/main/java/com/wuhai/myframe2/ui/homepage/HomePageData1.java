package com.wuhai.myframe2.ui.homepage;

public class HomePageData1 {

    private int type;
    private String name;

    public HomePageData1(int type, String name) {
        this.type = type;
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
